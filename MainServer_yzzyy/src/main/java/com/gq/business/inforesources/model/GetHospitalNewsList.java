package com.gq.business.inforesources.model;

public class GetHospitalNewsList {
	private String newsId;
	private String newsTitle;
	private String newsReleaseTime;
	private String newsContent;
	private String newsDigest;
	/**
	 * @return the newsId
	 */
	public String getNewsId() {
		return newsId;
	}
	/**
	 * @param newsId the newsId to set
	 */
	public void setNewsId(String newsId) {
		this.newsId = newsId;
	}
	/**
	 * @return the newsTitle
	 */
	public String getNewsTitle() {
		return newsTitle;
	}
	/**
	 * @param newsTitle the newsTitle to set
	 */
	public void setNewsTitle(String newsTitle) {
		this.newsTitle = newsTitle;
	}
	/**
	 * @return the newsReleaseTime
	 */
	public String getNewsReleaseTime() {
		return newsReleaseTime;
	}
	/**
	 * @param newsReleaseTime the newsReleaseTime to set
	 */
	public void setNewsReleaseTime(String newsReleaseTime) {
		this.newsReleaseTime = newsReleaseTime;
	}
	/**
	 * @return the newsContent
	 */
	public String getNewsContent() {
		return newsContent;
	}
	/**
	 * @param newsContent the newsContent to set
	 */
	public void setNewsContent(String newsContent) {
		this.newsContent = newsContent;
	}
	/**
	 * @return the newsDigest
	 */
	public String getNewsDigest() {
		return newsDigest;
	}
	/**
	 * @param newsDigest the newsDigest to set
	 */
	public void setNewsDigest(String newsDigest) {
		this.newsDigest = newsDigest;
	}
	
	
}
