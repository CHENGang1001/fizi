package com.gq.business.inforesources.model;

public class GetHealthCareList {
	private String careId;
	private String careTitle;
	private String careReleaseTime;
	private String careContent;
	private String careDigest;
	/**
	 * @return the careId
	 */
	public String getCareId() {
		return careId;
	}
	/**
	 * @param careId the careId to set
	 */
	public void setCareId(String careId) {
		this.careId = careId;
	}
	/**
	 * @return the careTitle
	 */
	public String getCareTitle() {
		return careTitle;
	}
	/**
	 * @param careTitle the careTitle to set
	 */
	public void setCareTitle(String careTitle) {
		this.careTitle = careTitle;
	}
	/**
	 * @return the careReleaseTime
	 */
	public String getCareReleaseTime() {
		return careReleaseTime;
	}
	/**
	 * @param careReleaseTime the careReleaseTime to set
	 */
	public void setCareReleaseTime(String careReleaseTime) {
		this.careReleaseTime = careReleaseTime;
	}
	/**
	 * @return the careContent
	 */
	public String getCareContent() {
		return careContent;
	}
	/**
	 * @param careContent the careContent to set
	 */
	public void setCareContent(String careContent) {
		this.careContent = careContent;
	}
	/**
	 * @return the careDigest
	 */
	public String getCareDigest() {
		return careDigest;
	}
	/**
	 * @param careDigest the careDigest to set
	 */
	public void setCareDigest(String careDigest) {
		this.careDigest = careDigest;
	}
	
}
