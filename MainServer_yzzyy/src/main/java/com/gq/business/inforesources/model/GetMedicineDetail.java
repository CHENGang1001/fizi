package com.gq.business.inforesources.model;

public class GetMedicineDetail {
	private String medicineId;
	private String medicineName;
	private String medicineSummary;
	private String medicineDetail;
	private String medicineClassification;
	private String manufacturer;
	/**
	 * @return the medicineId
	 */
	public String getMedicineId() {
		return medicineId;
	}
	/**
	 * @param medicineId the medicineId to set
	 */
	public void setMedicineId(String medicineId) {
		this.medicineId = medicineId;
	}
	/**
	 * @return the medicineName
	 */
	public String getMedicineName() {
		return medicineName;
	}
	/**
	 * @param medicineName the medicineName to set
	 */
	public void setMedicineName(String medicineName) {
		this.medicineName = medicineName;
	}
	/**
	 * @return the medicineSummary
	 */
	public String getMedicineSummary() {
		return medicineSummary;
	}
	/**
	 * @param medicineSummary the medicineSummary to set
	 */
	public void setMedicineSummary(String medicineSummary) {
		this.medicineSummary = medicineSummary;
	}
	/**
	 * @return the medicineDetail
	 */
	public String getMedicineDetail() {
		return medicineDetail;
	}
	/**
	 * @param medicineDetail the medicineDetail to set
	 */
	public void setMedicineDetail(String medicineDetail) {
		this.medicineDetail = medicineDetail;
	}
	/**
	 * @return the medicineClassification
	 */
	public String getMedicineClassification() {
		return medicineClassification;
	}
	/**
	 * @param medicineClassification the medicineClassification to set
	 */
	public void setMedicineClassification(String medicineClassification) {
		this.medicineClassification = medicineClassification;
	}
	/**
	 * @return the manufacturer
	 */
	public String getManufacturer() {
		return manufacturer;
	}
	/**
	 * @param manufacturer the manufacturer to set
	 */
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
}
